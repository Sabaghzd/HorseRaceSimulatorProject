    private void horse1CoatComboBoxActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_horse1CoatComboBoxActionPerformed
        // TODO add your handling code here:
        // Retrieve the selected coat from the combo box
        
        String selectedCoat = (String) horse1CoatComboBox.getSelectedItem();
        if (!selectedCoat.equals("Select Coat")) {
            ImageIcon icon = null;

            // Determine the ImageIcon based on the selected coat
            switch (selectedCoat) {
                case "Red Roan":
                    icon = new ImageIcon(getClass().getResource("/redRoan copy.png"));
                    break;
                case "Palomino":
                    icon = new ImageIcon(getClass().getResource("/palomino copy.png"));
                    break;
                case "Piebald":
                    icon = new ImageIcon(getClass().getResource("/pieBald copy.png"));
                    break;
                case "Buckskin":
                    icon = new ImageIcon(getClass().getResource("/buckskin copy.png"));
                default:
                    break;
            }

            // Debugging statements
            System.out.println("Selected coat: " + selectedCoat);
            System.out.println("Horse name: " + horse1NameField.getText());

            // Put the selected ImageIcon into the horseData map
            horseData.put(horse1NameField.getText(), icon);
        }
        
        
    }//GEN-LAST:event_horse1CoatComboBoxActionPerformed

    
    private void updateCoatComboBox(JTextField nameField, JComboBox<String> coatComboBox) {
        if (!nameField.getText().isEmpty()) {
            coatComboBox.setEnabled(true);
        } else {
            coatComboBox.setEnabled(false);
            coatComboBox.setSelectedIndex(0); // Reset selection when name field is cleared
        }
    }

