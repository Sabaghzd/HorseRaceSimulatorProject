    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
// TODO add your handling code here:
        JTextField[] horseNameFields = {horse1NameField, horse2NameField, horse3NameField, horse4NameField, horse5NameField};
        JTextField[] horseConfidenceFields = {horse1Confidence, horse2Confidence, horse3Confidence, horse4Confidence, horse5Confidence};
        List<JComboBox<String>> horseAccessories = new ArrayList<>();

        // Add JComboBox objects to the list
        horseAccessories.add(accesoriesbox1);
        horseAccessories.add(accesoriesbox2);
        horseAccessories.add(accesoriesbox3);
        horseAccessories.add(accesoriesbox4);
        horseAccessories.add(accesoriesbox5);
        

        for (int i = 0; i < horsesNum.getSelectedIndex()-1; i++) {
            
            if (horseNameFields[i].getText()!=null && horseConfidenceFields[i].getText()!=null) {  
                String horseName = horseNameFields[i].getText();
                String confidenceText = horseConfidenceFields[i].getText();
                double confidence = Double.parseDouble(confidenceText);
                if (horseAccessories.get(i).getSelectedItem() != "Select") {
                        // Assuming horseConfidence is the confidence field of the Horse class
                        // You need to replace it with your actual confidence field
                        confidence += 0.1;
                        if (confidence>1) {
                            confidence = 1;
                        }
                    }
                horseConfidence.put(horseName, confidence);
                   
                }
            }
        
       // Check if there are no valid horse names entered
        boolean allEmpty = true;
        int num=0;
        for (int i = 0; i < horsesNum.getSelectedIndex() - 1; i++) {
            if (!horseNameFields[i].getText().isEmpty()) {
                allEmpty = false;
                
            } else {
                num++;
            }
        }
        if (allEmpty || num==1 || lengthField.getText().isEmpty()) {
            // Show error message and return without proceeding
            JOptionPane.showMessageDialog(this, "Please enter at least two horse name and set a race length", "Invalid Input", JOptionPane.ERROR_MESSAGE);
            return;
        }

        // Create and show the race GUI
        System.out.println(horseData.toString());
        int len = Integer.parseInt(lengthField.getText()) > 130 ? 130 : Integer.parseInt(lengthField.getText());
        RaceGUI raceGUI = new RaceGUI(horseData, horseConfidence, len);
        raceGUI.setVisible(true);


        // Close the settings page
        dispose();
    }//GEN-LAST:event_jButton1ActionPerformed

