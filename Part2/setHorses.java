    private static class BecomingRedDocumentListener implements DocumentListener {
        private JTextField textField;
        private JButton setButton;

        public BecomingRedDocumentListener(JTextField textField, JButton setButton) {
            this.textField = textField;
            this.setButton = setButton;
        }

        @Override
        public void insertUpdate(DocumentEvent e) {
            updateComponents();
        }

        @Override
        public void removeUpdate(DocumentEvent e) {
            updateComponents();
        }

        @Override
        public void changedUpdate(DocumentEvent e) {
            updateComponents();
        }

        private void updateComponents() {
            String text = textField.getText();
            try {
                double value = Double.parseDouble(text);
                if (value < 0 || value > 1) {
                    textField.setBackground(Color.RED);
                    setButton.setEnabled(false); // Disable the set button
                } else {
                    textField.setBackground(Color.WHITE);
                    setButton.setEnabled(true); // Enable the set button
                }
            } catch (NumberFormatException ex) {
                textField.setBackground(Color.RED);
                setButton.setEnabled(false); // Disable the set button
            }
        }
    }


    
